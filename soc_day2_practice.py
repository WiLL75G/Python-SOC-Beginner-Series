# Practice: combining log type and message



log_type = "SSH"
message = "failed login"

print(log_type + " " + message)



# Output:
# SSH failed login

# Explanation:
# - Variables store text (strings)
# - + combines text into one message
# - Helps SOC analysts read and understand logs quickly